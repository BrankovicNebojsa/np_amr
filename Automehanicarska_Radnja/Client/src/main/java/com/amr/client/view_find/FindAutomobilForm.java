    private void jButtonIzaberiAutomobilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIzaberiAutomobilActionPerformed
        if (jTableAutomobili.getSelectedRow() != -1) {
            Automobil automobil = ((SearchAutomobilTableModel) jTableAutomobili.getModel()).getAutomobili().get(jTableAutomobili.getSelectedRow());
            this.setVisible(false);
            arf.setAutomobil(automobil);
            arf.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Izaberite automobil");
        }
    }//GEN-LAST:event_jButtonIzaberiAutomobilActionPerformed

