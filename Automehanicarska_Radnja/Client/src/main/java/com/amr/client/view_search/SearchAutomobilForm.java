    private void jButtonServisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonServisActionPerformed
        if (jTableAutomobili.getSelectedRow() != -1) {
            Automobil automobil = ((SearchAutomobilTableModel) jTableAutomobili.getModel()).getAutomobili().get(jTableAutomobili.getSelectedRow());
            Servis servis = new Servis();
            servis.setAutomobil(automobil);
            servis = c.getServis(servis);
            if (servis == null) {
                JOptionPane.showMessageDialog(this, "Za izabrani automobil ne postoji servis");
            } else {
                jTextFieldServis.setText(servis.getDatumServisa().toString());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Izaberite automobil");
        }
    }//GEN-LAST:event_jButtonServisActionPerformed

