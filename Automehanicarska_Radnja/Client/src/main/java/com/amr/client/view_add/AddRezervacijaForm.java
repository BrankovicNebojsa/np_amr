    private void jButtonZapamtiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonZapamtiActionPerformed
        if (!((AddRezervacijaTableModel) jTableRezervacije.getModel()).getRezervacije().isEmpty()) {
            List<Rezervacija> rezervacije = ((AddRezervacijaTableModel) jTableRezervacije.getModel()).getRezervacije();
            if (!c.saveRezervacije(rezervacije)) {
                JOptionPane.showMessageDialog(this, "Greska pri unosu rezervacije."
                        + " Vec postoji rezervacija u tom periodu kod izabranog radnika", "Greska", JOptionPane.ERROR_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Unete rezervacije su zapamcene.");
            }

        } else {
            JOptionPane.showMessageDialog(this, "Tabela je prazna");
        }
        this.setVisible(false);
        this.dispose();
    }//GEN-LAST:event_jButtonZapamtiActionPerformed

