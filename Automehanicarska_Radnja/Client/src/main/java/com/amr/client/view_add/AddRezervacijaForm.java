    private void jButtonDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDodajActionPerformed
        if (isDataValid()) {
            Rezervacija rezervacija = new Rezervacija();
            rezervacija.setTrajanjeServisa(Integer.parseInt(jTextFieldTrajanjeServisa.getText()));
            rezervacija.setAutomobil(automobil);
            rezervacija.setRadnik(findRadnik());
            String datumRezervacijeString = jTextFieldDatumRezervacije.getText().trim() + " " + jTextFieldSat.getText().trim()
                    + ":" + jTextFieldMinut.getText().trim() + ":00";
            Date datumRezervacije;
            try {
                datumRezervacije = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").parse(datumRezervacijeString);
                rezervacija.setPocetakServisa(datumRezervacije);
            } catch (ParseException ex) {
                System.out.println("Greska pri parsiranju kod jButtonZapamti u AddRezervacijaForm");
                return;
            }
            ((AddRezervacijaTableModel) jTableRezervacije.getModel()).addRezervacija(rezervacija);
            resetData();
        }
    }//GEN-LAST:event_jButtonDodajActionPerformed

